const mongoose = require('mongoose');

// Skema untuk model produk
const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  brand: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
});

// Export dan buat model
module.exports = mongoose.model('Product', productSchema);
